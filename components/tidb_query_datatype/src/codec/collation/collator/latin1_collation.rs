use std::marker::PhantomData;

use super::*;

const TABLE_SIZE_FOR_LATIN1: usize = 256;

trait Latin1Collator: 'static + Send + Sync + std::fmt::Debug {
    const IS_CASE_INSENSITIVE: bool;
    const WEIGHT_TABLE: &'static [u8; TABLE_SIZE_FOR_LATIN1];
}

#[derive(Debug)]
pub struct CollatorLatin1<C>(PhantomData<C>);

#[derive(Debug)]
pub struct Latin1SwedishCi;

impl Latin1Collator for Latin1SwedishCi {
    const IS_CASE_INSENSITIVE: bool = true;
    const WEIGHT_TABLE: &'static [u8; TABLE_SIZE_FOR_LATIN1] = &LATIN1_SWEDISH_CI_WEIGHT_TABLE;
}

pub type CollatorLatin1SwedishCi = CollatorLatin1<Latin1SwedishCi>;

impl<C: Latin1Collator> Collator for CollatorLatin1<C> {
    type Charset = CharsetLatin1;
    type Weight = u8;

    const IS_CASE_INSENSITIVE: bool = C::IS_CASE_INSENSITIVE;

    #[inline]
    fn char_weight(ch: char) -> Self::Weight {
        if let Some(b) = unicode_to_latin1(ch) {
            C::WEIGHT_TABLE[usize::from(b)]
        } else {
            b'?'
        }
    }

    #[inline]
    fn write_sort_key<W: BufferWriter>(writer: &mut W, bstr: &[u8]) -> Result<usize> {
        let mut bstr_rest = trim_end_padding(bstr);
        let mut n = 0;

        while let Some((ch, b_next)) = next_utf8_char(bstr_rest) {
            writer.write_u8(Self::char_weight(ch))?;
            n += 1;
            bstr_rest = b_next;
        }
        Ok(n)
    }

    #[inline]
    fn sort_compare(a: &[u8], b: &[u8], force_no_pad: bool) -> Result<Ordering> {
        let a = if force_no_pad { a } else { trim_end_padding(a) };
        let b = if force_no_pad { b } else { trim_end_padding(b) };

        let mut a_rest = a;
        let mut b_rest = b;

        while !a_rest.is_empty() && !b_rest.is_empty() {
            match (next_utf8_char(a_rest), next_utf8_char(b_rest)) {
                (Some((ch_a, a_next)), Some((ch_b, b_next))) => {
                    let ord = Self::char_weight(ch_a).cmp(&Self::char_weight(ch_b));
                    if ord != Ordering::Equal {
                        return Ok(ord);
                    }
                    a_rest = a_next;
                    b_rest = b_next;
                }
                // this is the NEED_TRUNCATE_INVALID_UTF8_RUNE behavior.
                _ => return Ok(Ordering::Equal),
            }
        }

        Ok(a_rest.len().cmp(&b_rest.len()))
    }

    #[inline]
    fn sort_hash<H: Hasher>(state: &mut H, bstr: &[u8]) -> Result<()> {
        let mut bstr_rest = trim_end_padding(bstr);
        while let Some((ch, b_next)) = next_utf8_char(bstr_rest) {
            Self::char_weight(ch).hash(state);
            bstr_rest = b_next;
        }
        Ok(())
    }
}

#[inline]
fn unicode_to_latin1(ch: char) -> Option<u8> {
    Some(match ch {
        '\u{0000}'..='\u{007f}' | '\u{00a0}'..='\u{00ff}' => ch as u8,
        '€' => 0x80,
        '‚' => 0x82,
        'ƒ' => 0x83,
        '„' => 0x84,
        '…' => 0x85,
        '†' => 0x86,
        '‡' => 0x87,
        'ˆ' => 0x88,
        '‰' => 0x89,
        'Š' => 0x8a,
        '‹' => 0x8b,
        'Œ' => 0x8c,
        'Ž' => 0x8e,
        '‘' => 0x91,
        '’' => 0x92,
        '“' => 0x93,
        '”' => 0x94,
        '•' => 0x95,
        '–' => 0x96,
        '—' => 0x97,
        '˜' => 0x98,
        '™' => 0x99,
        'š' => 0x9a,
        '›' => 0x9b,
        'œ' => 0x9c,
        'ž' => 0x9e,
        'Ÿ' => 0x9f,
        _ => return None,
    })
}

